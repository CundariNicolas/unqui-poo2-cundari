package tpComposite.ejercitos;

public class Posicion {
	private int posicionX;
	private int posicionY;
	private boolean tieneAlaja;
	
	public int getPosicionX() {
		return posicionX;
	}
	public int getPosicionY() {
		return posicionY;
	}
	public boolean isTieneAlaja() {
		return tieneAlaja;
	}
	public void setTieneAlaja(boolean tieneAlaja) {
		this.tieneAlaja = tieneAlaja;
	}
	
	
	
}
